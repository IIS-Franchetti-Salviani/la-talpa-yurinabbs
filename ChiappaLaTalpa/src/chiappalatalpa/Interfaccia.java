        labelColpi.setText("Colpi: 0");
        labelTempo.setText("Tempo: 30");

        int velocita = 1500;

        String difficolta = comboDifficolta.getSelectedItem().toString();

        if(difficolta.equals("Facile"))
            velocita = 1500;

        if(difficolta.equals("Media"))
            velocita = 1000;

        if(difficolta.equals("Difficile"))
            velocita = 600;

        talpa = new Talpa(buche, velocita);

        threadTalpa = new Thread(talpa);
        threadTalpa.start();

        avviaTimer();


