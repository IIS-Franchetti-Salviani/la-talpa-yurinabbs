    private void colpisciBuca(JButton b){ //METODO IMPLEMENTATO CON AI

        if(b.getText().equals("🐹")){
            colpi++;
            punteggio += 10;

        }else{

            punteggio -= 5;
            if (punteggio < 0){
                punteggio = 0;
            }

        }

        labelPunteggio.setText("Punteggio: " + punteggio);
        labelColpi.setText("Colpi: " + colpi);

    }
    private void avviaTimer(){

    threadTempo = new Thread(() -> {

        while(tempo > 0){

            try{

                Thread.sleep(1000);

                tempo--;

                labelTempo.setText("Tempo: " + tempo);

            }catch(Exception e){
                e.printStackTrace();
            }

        }

        fineGioco();

    });

    threadTempo.start();

}
    
    private void fineGioco(){

    talpa.ferma();

    classifica.salvaPunteggio(nomeGiocatore, punteggio);

    javax.swing.JOptionPane.showMessageDialog(
            this,
            "Tempo scaduto!\nPunteggio: " + punteggio
    );

    classifica.stampaClassifica();

}

